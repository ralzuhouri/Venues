import SwiftUI

class AppFonts {
    static var veryBig: Font {
        return .system(size: 32)
    }
    
    static var big: Font {
        return .system(size: 24)
    }
    
    static var regularHeavy: Font {
        return .system(size: 18, weight: .heavy)
    }
    
    static var regularMedium: Font {
        return .system(size: 16, weight: .medium)
    }
    
    static var regularLight: Font {
        return .system(size: 16, weight: .light)
    }
    
    static var small: Font {
        return .system(size: 12, weight: .light)
    }
}
